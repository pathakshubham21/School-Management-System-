         The Punjab School Education Board came into existence through a legislative enactment in November 1969 for the development and promotion of school education in the state of Punjab. In 1987, the Vidhan Sabha amended the Board’s Act to grant it autonomy. The scope of Board’s functions is very wide and covers almost every aspect/stage of school education. However, a brief account of functions, structure and activities of the Board is enumerated as below:<br><br>

<b>Functions Of The Board</b><br>
<br>
1.To make necessary arrangements for smooth conduct of public examination at school level .At present the Board conducts public examinations at Middle, Matriculation and Senior Secondary Level.<br>
2.To prescribe the curriculum, courses of studies and text books for school education. The Board has a full- fledged academic wing, having subject experts in all the major subjects. The main responsibility of this wing is to prepare and revise the syllabuses regularly and then prepare text books accordingly.
<br>3.To arrange for the preparations, compilation, improvement, publication, printing and sale of text books.
<br>4.To make necessary arrangements for affiliations of schools to the Board.
<br>5.To make efforts to bring about qualitative improvement in school education.
<br>6.To act as an advisory body to the state Govt. regarding School Education. this Act.

